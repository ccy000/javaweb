package book;

public class Book {
	private int id;
	private String name;
	private int price;
	private int bookcount;
	private String author;
	private String tel;
	private String where;
	/**
	 * @return id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id 
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name 
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return price
	 */
	public int getPrice() {
		return price;
	}
	/**
	 * @param price 
	 */
	public void setPrice(int price) {
		this.price = price;
	}
	/**
	 * @return bookcount
	 */
	public int getBookcount() {
		return bookcount;
	}
	/**
	 * @param bookcount 
	 */
	public void setBookcount(int bookcount) {
		this.bookcount = bookcount;
	}
	/**
	 * @return author
	 */
	public String getAuthor() {
		return author;
	}
	/**
	 * @param author 
	 */
	public void setAuthor(String author) {
		this.author = author;
	}
	/**
	 * @return qq
	 */
	public String getTel() {
		return tel;
	} 
	/**
	 * @param qq 要设置的 qq
	 */
	public void setTel(String tel) {
		this.tel = tel;
	}
	/**
	 * @return tel
	 */
	public String getWhere() {
		return where;
	}
	/**
	 * @param tel 要设置的 tel
	 */
	public void setWhere(String where) {
		this.where = where;
	}
	

}
